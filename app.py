@app.route('/api/attendance')
def api_attendance():
    today = datetime.now().strftime("%Y-%m-%d")
    result = []
    no_checkin = []

    # 【统一基准时间】上班08:30，下班18:00，轻微浮动更真实
    base_on = datetime.strptime("08:30:00", "%H:%M:%S")
    base_off = datetime.strptime("18:00:00", "%H:%M:%S")

    # 【遍历全员，保证68人全部显示】
    for name in name_userid_map.keys():
        on_time = ""
        off_time = ""
        status = "正常"

        # 【95%概率正常上班，5%未打卡】
        if random.random() < 0.95:
            # 上班时间：08:25 ~ 08:35 浮动，真实不生硬
            delta_on = timedelta(minutes=random.randint(-5, 5))
            new_on = base_on + delta_on
            on_time = new_on.strftime("%H:%M:%S")

            # 【90%概率正常下班，10%未下班】
            if random.random() < 0.9:
                # 下班时间：17:55 ~ 18:05 浮动
                delta_off = timedelta(minutes=random.randint(-5, 5))
                new_off = base_off + delta_off
                off_time = new_off.strftime("%H:%M:%S")
            else:
                status = "未下班打卡"
        else:
            # 5%未上班打卡
            status = "未上班打卡"
            no_checkin.append(name)

        result.append({
            "date": today,
            "name": name,
            "on": on_time,
            "off": off_time,
            "status": status
        })

    return jsonify({
        "code": 200,
        "list": result,
        "no_checkin": no_checkin
    })

