def get_access_token():
    url = f"https://oapi.dingtalk.com/gettoken?appkey={APP_KEY}&appsecret={APP_SECRET}"
    res = requests.get(url)
    result = res.json()
    if result.get("errcode") != 0:
        raise Exception(f"获取token失败: {result}")
    return result["access_token"]

